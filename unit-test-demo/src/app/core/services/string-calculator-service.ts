export class StringCalculatorService {

  constructor() {

  }

  public add(input: string): string {
    // if (input === "") {
    //   return "0";
    // }

    if (input.length) {
        let parts = input.split(',');
        let final = 0;
        parts.forEach((e : string) => {
            
            let n = parseFloat(e);
            if(!isNaN(n)) {
                final += n;
            }
        }); 

      return final.toString();
    } else return '0';
  }
}
